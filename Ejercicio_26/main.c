#include <stdio.h>
#include <string.h>
#include <ctype.h>

int main() {
    char palabra[100];
    int vocales = 0;
    int consonantes = 0;
    int i;

    printf("Ingresa una palabra en minusculas: ");

    scanf(" %99s", palabra);

    for (i = 0; palabra[i] != '\0'; i++) {
        char caracter = palabra[i];
        if (isalpha(caracter)) {

            switch (caracter) {
                case 'a':
                case 'e':
                case 'i':
                case 'o':
                case 'u':
                    vocales++;
                    break;
                default:

                    consonantes++;
                    break;
            }
        }

    }

    printf("\n--- Resultados ---\n");
    printf("Cantidad de vocales: %d\n", vocales);
    printf("Cantidad de consonantes: %d\n", consonantes);

    return 0;
}

