/*   Ejercicio Nº 53 Estructura Repetitiva Do While : Escribir un programa que solicite la carga de un número entre 0 y 999, y
                    nos muestre un mensaje de cuántos dígitos tiene el mismo. Finalizar el programa cuando se cargue el valor 0.
/*_____________________________________________________________________________________________________________________________________________________________________________________________________________________________*/

#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>


int main()
{

int numero;




do {
printf("Introduce un número entre 0 y 999 \n");
scanf("%d", &numero) ;

if(numero>0 && numero<=9){
      printf("Tiene un Digito\n");
} else
if(numero>9 && numero<=99){
      printf("Tiene dos Digito\n");
} else
if(numero>99 && numero<=999){
      printf("Tiene tres Digito\n");
} else
      printf("Fuera de Rango debe estar entre 0 y 999\n");

} while (numero != 0);



    return 0;
}

