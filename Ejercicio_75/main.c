#include <stdio.h>

float celsiusAFahrenheit(float celsius);

int main() {
    float temp_celsius;
    float temp_fahrenheit;

    printf("--- Conversor de Temperatura ---\n");
    printf("Ingrese la temperatura en grados Celsius: ");
    scanf("%f", &temp_celsius);

    temp_fahrenheit = celsiusAFahrenheit(temp_celsius);

    printf("%.2f grados Celsius equivalen a %.2f grados Fahrenheit.\n", temp_celsius, temp_fahrenheit);

    return 0;
}

float celsiusAFahrenheit(float celsius) {
    float fahrenheit;
    fahrenheit = (celsius * 9.0 / 5.0) + 32.0;
    return fahrenheit;
}

