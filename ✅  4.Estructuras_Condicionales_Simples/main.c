#include <stdio.h>
#include <stdlib.h>

int main() {
    int edad = 20;

    printf("--- Estructura Condicional Simple: if ---\n");

    if (edad >= 18) {
        printf("Eres mayor de edad.\n");
    }

    int temperatura = 15;
    if (temperatura > 25) {
        printf("Hace calor.\n");
    }

    return 0;
}

