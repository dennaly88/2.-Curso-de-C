#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>



int main()
{
    int dia;

printf("---------------------------------------------------------------------\n");
printf("Determinar el dia de la Semana                                        :");
printf("\n-------------------------------------------------------------------\n");



    printf("Ingrese el numero del día: ");
    fflush(stdin);
    scanf("%d", &dia);

  switch (dia) {
        case 1:
            printf("El Día es Lunes");
            break;
        case 2:
            printf("El Día es Martes");
            break;
        case 3:
            printf("El Día es Miercoles");
            break;
        case 4:
            printf("El Día es Jueves");
            break;
        case 5:
            printf("El Día es Viernes");
            break;
        case 6:
            printf("El Día es Sabado");
            break;
         case 7:
            printf("El Día es Domingo");
            break;
        default:
            printf("El numero no Corresponde a un Día Valido.\n");
            break;
    }





    return 0;
}

