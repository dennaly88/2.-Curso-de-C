#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>



int main()
{

int num;

printf("---------------------------------------------------------------------\n");
printf("Ingresar por teclado un valor entero comprendido entre 1 y 5:");
printf("\n-------------------------------------------------------------------\n");

scanf("%d", &num) ;


switch (num) {
    case (1):
         printf("El numero es Uno");
         break;
    case (2):
         printf("El numero es Dos");
         break;
    case (3):
         printf("El numero es Tres");
         break;
    case (4):
         printf("El numero es Cuatro");
         break;
    case (5):
         printf("El numero es Cinco");
         break;
    default:
        printf("Se cargó un valor fuera de rango");
        break;
}

    return 0;
}

