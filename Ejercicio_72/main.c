#include <stdio.h>
#define PI 3.14159

float calcularAreaCirculo(float radio);

int main() {
    float radio_ingresado;
    float area_calculada;

    printf("Ingrese el radio del círculo: ");
    scanf("%f", &radio_ingresado);

    area_calculada = calcularAreaCirculo(radio_ingresado);

    printf("El área del círculo con radio %.2f es: %.2f\n", radio_ingresado, area_calculada);

    return 0;
}

float calcularAreaCirculo(float radio) {
    float area;
    area = PI * radio * radio;
    return area;
}

