#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>



int main()
{
    int mes;

printf("---------------------------------------------------------------------\n");
printf("Determinar el Mes del Año donde estas el dia de hoy                   :");
printf("\n-------------------------------------------------------------------\n");



    printf("Ingrese el numero del mes : ");
    fflush(stdin);
    scanf("%d", &mes);

  switch (mes) {
        case 1:
            printf("El mes es Enero");
            break;
        case 2:
            printf("El mes es Febrero");
            break;
        case 3:
            printf("El mes es Marzo");
            break;
        case 4:
            printf("El mes es Abril");
            break;
        case 5:
            printf("El mes es Mayo");
            break;
        case 6:
            printf("El mes es Junio");
            break;
         case 7:
            printf("El mes es Julio");
            break;
        case 8:
            printf("El mes es Agosto");
            break;
        case 9:
            printf("El mes es Septiembre");
            break;
        case 10:
            printf("El mes es Octubre");
            break;
        case 11:
            printf("El mes es Noviembre");
            break;
        case 12:
            printf("El mes es Diciembre");
            break;
        default:
            printf("El numero no Corresponde a un Mes Valido.\n");
            break;
    }





    return 0;
}

