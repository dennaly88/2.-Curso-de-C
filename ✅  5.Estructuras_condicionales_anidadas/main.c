#include <stdio.h>

int main() {
    int numero = 7;

    printf("\n--- Estructura Condicional Compuesta: if-else ---\n");

    if (numero % 2 == 0) {
        printf("%d es un número par.\n", numero);
    } else  {
        printf("%d es un número impar.\n", numero);
    }

    return 0;
}
