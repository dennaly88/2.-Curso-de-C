/*   Ejercicio Nº 10 Estructuras Condicionales Simples : Mostrar un mensaje indicando si el número tiene uno o dos dígitos
/*________________________________________________________________________________________________________________________________*/

#include <stdio.h>

int main() {
    int numero;

    printf("Ingrese un numero: ");
    scanf("%d", &numero);

    if ((numero >= 1 && numero <= 9) || (numero <= -1 && numero >= -9)) {
        printf("El numero tiene un digito.\n");
    }
    else if ((numero >= 10 && numero <= 99) || (numero <= -10 && numero >= -99)) {
        printf("El numero tiene dos digitos.\n");
    }
    else {
        printf("El numero no tiene uno ni dos digitos (o es 0 o tiene mas de dos digitos).\n");
    }

    return 0;
}

