/*   Ejercicio Nº 67 Vectores y Matrices : Desarrollar un programa que permita cargar 5 nombres de personas y sus edades respectivas.
       Luego de realizar la carga por teclado de todos los datos imprimir los nombres de las
       personas mayores de edad (mayores o iguales a 18 años)
/*_____________________________________________________________________________________________________________________________________________________________________________________________________________*/

#include <stdio.h>
#include <string.h>

#define MAX_PERSONAS 5
#define MAX_NOMBRE 50

int main() {
    char nombres[MAX_PERSONAS][MAX_NOMBRE];
    int edades[MAX_PERSONAS];
    int i ;

    printf("--- Carga de datos de personas ---\n");

    for (i = 0; i < MAX_PERSONAS; i++) {
        printf("\nIngrese el nombre de la persona %d: ", i + 1);
        fgets(nombres[i], MAX_NOMBRE, stdin);
        nombres[i][strcspn(nombres[i], "\n")] = 0;

        printf("Ingrese la edad de %s: ", nombres[i]);
        scanf("%d", &edades[i]);
        while (getchar() != '\n');
    }

    printf("\n--- Personas mayores de edad (18 años o más) ---\n");

    for (i = 0; i < MAX_PERSONAS; i++) {
        if (edades[i] >= 18) {
            printf("- %s (Edad: %d)\n", nombres[i], edades[i]);
        }
    }

    return 0;
}
